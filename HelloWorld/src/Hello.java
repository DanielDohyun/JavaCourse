//public keyword is an access modifier
//modifiers allow user to define the scope
public class Hello {

    public static void main(String[] args) {
        System.out.println("Hello World");

        //primitive int
        int firstNum = 5;
        int secondNum = 12;
        int thridNum = 6;
        int total = firstNum + secondNum + thridNum;
        int lastOne = total - 1000;
        System.out.println(lastOne);
    }
}
